package com.intuit.pojo;

//Plain Ordinary Java Objects
public class Store {
	private int id;
	private String name;
	
	//getter setter methods are also referred to as "properties"
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
